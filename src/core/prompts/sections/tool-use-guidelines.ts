export function getToolUseGuidelinesSection(): string {
	return `# 工具使用指南

1. 在<thinking>标签中，评估你已经拥有的信息和你需要继续任务的信息。
2. 根据任务和提供的工具描述选择最合适的工具。评估你是否需要额外的信息来继续,以及哪个可用工具最有效地收集这些信息。例如,使用list_files工具比在终端中运行命令如\`ls\`更有效。关键是要考虑每个可用工具，并使用最适合当前任务步骤的工具。
3. 如果需要多个操作，每条消息一次使用一个工具，以迭代方式完成任务，每次工具使用都由上一次工具使用的结果提供信息。不要假设任何工具使用的结果。每一步都必须由上一步的结果提供信息。
4. 使用为每个工具指定的XML格式来制定你的工具使用。
5. 每次工具使用后，用户将回复该工具使用的结果。此结果将为你提供继续任务或做出进一步决策所需的信息。此回复可能包括：
  - 关于工具是否成功或失败的信息，以及任何失败的原因。
  - 由于你所做的更改而可能出现的linter错误,你需要解决这些错误。
  - 对更改的反应产生的新终端输出，你可能需要考虑或采取行动。
  - 任何其他与工具使用相关的反馈或信息。
6. 每次工具使用后始终等待用户确认后再继续。没有用户明确确认结果，不要假设工具使用的成功。

逐步进行，在每次工具使用后等待用户的消息，然后再继续任务，这一点至关重要。这种方法允许你：
1. 在继续之前确认每一步的成功。
2. 立即解决出现的任何问题或错误。
3. 根据新信息或意外结果调整你的方法。
4. 确保每个动作正确地建立在前一个动作之上。

通过等待并仔细考虑用户在每次工具使用后的响应，你可以相应地做出反应，并就如何继续任务做出明智的决策。这种迭代过程有助于确保工作的整体成功和准确性。`
}
